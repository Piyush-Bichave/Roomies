const mongoose = require("mongoose");

// Define the Room Schema
const roomSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
});

// Create a Room model using the schema
module.exports = mongoose.model("Room", roomSchema);
